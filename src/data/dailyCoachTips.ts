export const getRandomTip = (): DailyTip => {
  const randomIndex = Math.floor(Math.random() * dailyCoachTips.length);
  return dailyCoachTips[randomIndex];
};

export const getTipForToday = (): DailyTip => {
  // Get a consistent tip for the day based on the date
  const today = new Date();
  const dayOfYear = Math.floor(
    (today.getTime() - new Date(today.getFullYear(), 0, 0).getTime()) / (1000 * 60 * 60 * 24)
  );
  const tipIndex = dayOfYear % dailyCoachTips.length;
  return dailyCoachTips[tipIndex];
};
